        //entao um carta aleatoria é adicionada nele
        cartasNaMaoAjuste[0] = cartasEmbaralhadas[0];
        //apos isso o vetor que contem as cartas da loja é reordenado
        reordenarVetor(0, cartasEmbaralhadas);
